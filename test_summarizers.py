from tfidf_summarizer import summarize_document
from textrank_summarizer import summarize_textrank

# 테스트할 텍스트
text = '호주의 광활한 마일리언스 사막에서 최근 발견된 거대 수정 구조체가 전 세계의 과학자들과 신비 연구가들의 이목을 집중시키고 있다. 이 수정 구조체는 정확히 말해 직경 15미터, 높이 30미터에 달하는 거대한 기둥 모양의 결정체로, 햇빛을 받아 반짝이며 사막의 한가운데에서 신비로운 장관을 연출하고 있다. 지난주 호주 지질연구소(HGS)는 이 수정 구조체가 발견되었다고 발표하며, 현재까지 이에 대한 공식적인 설명은 내놓지 못하고 있다고 밝혔다. 현장 조사에 참여한 연구원들의 말에 따르면, 이 구조체는 일반적인 수정이나 석영과는 달리 복잡한 분자 구조를 지니고 있으며, 특정한 빛의 파장에서 형광을 방출하는 특이한 성질을 가지고 있다고 한다. 이 수정은 지표면에서 약간 솟아올라 있어, 마치 고대 문명의 유적처럼 보인다. 현장 조사팀의 리더인 줄리아 캠벨 박사는 "이렇게 거대한 자연 수정 구조체가 인류의 역사에서 발견된 적은 거의 없다. 우리는 이 구조체가 자연적으로 형성된 것인지, 혹은 과거 어느 문명이 인위적으로 만든 것인지 명확히 밝히기 위해 조사를 이어갈 것"이라고 밝혔다. 그녀는 또한 구조체의 방사성 탄소 연대 측정이 현재 진행 중이며, 그 결과에 따라 고고학적 의미도 추가적으로 검토될 것이라고 덧붙였다. 이 신비로운 발견은 많은 이론을 불러일으키고 있다. 일부 연구가들은 이 수정 구조체가 고대 문명의 유산일 가능성을 제기하고 있다. 특히 호주의 원주민 전설 중 하나인 \'반짝이는 하늘의 기둥\' 이야기가 이번 발견과 일치하는 부분이 있어, 많은 이들이 이 구조체가 전설 속에서 언급된 신비로운 물체가 아니냐는 추측을 내놓고 있다. 반면, 몇몇 과학자들은 이 수정이 드물게 발생하는 지각 변동과 화학적 요인들이 결합되어 형성된 자연적 산물일 가능성이 높다고 보고 있다. 한편, 이번 수정 구조체의 발견 소식은 이미 전 세계의 관광객들의 관심을 끌고 있다. 여러 여행사들은 현장을 관광 명소로 개발할 수 있는 가능성을 모색 중이며, 일부 단체는 구조체 주변의 생태계 보호 문제를 제기하며 신중한 접근을 촉구하고 있다. 이와 관련해 호주 정부는 수정 구조체 주변 지역을 임시 보호구역으로 지정하고, 연구가 완료될 때까지 일반인의 접근을 제한한다고 발표했다. 연구팀은 향후 몇 달간 수정의 기원을 밝히기 위해 추가적인 실험과 분석을 진행할 예정이다. 이번 발견이 인류의 역사와 지구의 형성에 대한 새로운 단서를 제공할 수 있을지, 전 세계의 관심이 집중되고 있다. 수정 구조체에 얽힌 비밀이 밝혀질 때까지, 이 신비로운 사막의 거대 수정은 계속해서 많은 이들의 상상력을 자극할 것으로 보인다.'

# TF-IDF 기반 요약 테스트
print("TF-IDF Summarization:")
summary_tfidf, error = summarize_document(text, num_sentences=3)
if error:
    print("Error:", error)
else:
    print(summary_tfidf)

# TextRank 기반 요약 테스트
print("\nTextRank Summarization:")
summary_textrank = summarize_textrank(text, num_sentences=3)
print(" ".join(summary_textrank))
